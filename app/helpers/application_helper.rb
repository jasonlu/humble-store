def button_link_to(*sources)
  
  html_options = {class: "btn btn-default"}
  if (sources[2].kind_of?(Hash))
    sources[2].reverse_merge!(html_options)  
  else
    sources[2] = html_options
  end
  
  link_to *sources
  
end

